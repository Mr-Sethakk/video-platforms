async function main() {
  const conn = await mysql.createConnection({
    host: '127.0.0.1', port: 3306,
    user: 'root', password: 'root',
    database: 'movie_platform', charset: 'utf8mb4',
  });

  let updated = 0;
  for (const [doubanId, title, director, actors, duration, country, description] of MOVIES) {
    const [res] = await conn.query(
      `UPDATE movie SET director=?, actors=?, duration=?, country=?, description=?
       WHERE douban_id=? AND (director IS NULL OR director='')`,
      [director, actors, duration, country, description, String(doubanId)]
    );
    if (res.affectedRows > 0) updated++;
  }

  // Also update any movies that share a title but weren't matched by douban_id
  for (const [, title, director, actors, duration, country, description] of MOVIES) {
    const [res] = await conn.query(
      `UPDATE movie SET director=?, actors=?, duration=?, country=?, description=?
       WHERE title=? AND (director IS NULL OR director='')`,
      [director, actors, duration, country, description, title]
    );
    if (res.affectedRows > 0) updated++;
  }

  const [s] = await conn.query(
    `SELECT COUNT(*) as t, SUM(description IS NOT NULL AND description!='') as d,
            SUM(director IS NOT NULL AND director!='') as r FROM movie`
  );
  console.log(`Updated: ${updated}`);
  console.log(`Total: ${s[0].t} | Desc: ${s[0].d} | Dir: ${s[0].r}`);

  // Show a sample
  const [rows] = await conn.query('SELECT id, title, director, LEFT(description, 40) as desc_preview FROM movie LIMIT 5');
  for (const r of rows) console.log(`  ${r.title} | ${r.director} | ${r.desc_preview}...`);

  await conn.end();
  console.log('DONE');
}

main().catch(e => { console.error(e); process.exit(1); });
